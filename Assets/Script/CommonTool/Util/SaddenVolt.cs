using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SaddenVolt
{
    public static string EthnicUpStr(double a)
    {
        return Math.Round(a, BureauChoppy.MeritCrouch).ToString();
    }
    public static string EthnicUpStr(double a, int digits)
    {
        return Math.Round(a, digits).ToString();
    }

    public static double Merit(double a)
    {
        return Math.Round(a, BureauChoppy.MeritCrouch);
    }

}
